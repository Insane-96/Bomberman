		public static void DrawNumber(byte number, Window window, int x, int y)
		{
			if (number > 9)
				return;
			else
			{
				string[] split = numbers[number].Split(',');
				for (int i = 0; i < split.Length; i++)
				{
					for (int p = 0; p < split[i].Length; p++)
					{
						if (split[i][p] == '1')
						PutPixel(window, 0, 0, 0, x + p, y + i);
					}
				}
			}
		}
